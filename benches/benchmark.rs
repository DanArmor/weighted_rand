// Weighted random sampling with Cumulative Distribution Function

struct CDF {
    probs: Vec<f32>,
}

impl CDF {
    fn next(&self) -> usize {
        let r = rand::thread_rng().gen::<f32>();
        let mut result = 0;

        for (i, p) in self.probs.iter().enumerate() {
            if r <= *p {
                result = i;
                break;
            }
        }

        result
    }
}
