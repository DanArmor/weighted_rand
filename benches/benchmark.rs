use rand::prelude::*;
use weighted_rand::builder::*;

use criterion::Criterion;
use criterion::{criterion_group, criterion_main};

fn bench_constructor(c: &mut Criterion) {
    let vector = WEIGHTS.to_vec();
    c.bench_function("constructor", |b| {
        b.iter(|| WalkerTableBuilder::new(&vector))
    });
}

fn bench_generate_by_wam(c: &mut Criterion) {
    let builder = WalkerTableBuilder::new(&WEIGHTS.to_vec());
    let table = builder.build();

    let mut rng = rand::thread_rng();

    c.bench_function("generate_by_wam", |b| {
        b.iter(|| {
            for _ in 0..100_000 {
                table.next_rng(&mut rng);
            }
        })
    });
}

fn bench_generate_by_cdf(c: &mut Criterion) {
    let mut probs = Vec::new();
    for i in 0..1000 {
        probs.push(i);
    }
    let probs = probs
        .iter()
        .map(|v| *v as f32 / 1000.0)
        .collect::<Vec<f32>>()
        .to_vec();

    let cdf = CDF { probs: probs };

    c.bench_function("generate_by_cdf", |b| {
        b.iter(|| {
            for _ in 0..100_000 {
                cdf.next();
            }
        })
    });
}

criterion_group!(
    benches,
    bench_constructor,
    bench_generate_by_wam,
    bench_generate_by_cdf
);
criterion_main!(benches);

